#!/bin/bash
# ═══════════════════════════════════════════════════════════════════════════════
# agent_name_ja.sh — エージェント名 英語 → 日本語 変換ライブラリ
# ═══════════════════════════════════════════════════════════════════════════════
# 内部識別子（@agent_id, ファイルパス, inbox routing）は英語のまま保持し、
# 表示系スクリプトのみでこのライブラリを使って変換する。
#
# 使用方法:
#   source scripts/agent_name_ja.sh
#   display_name=$(to_ja "ashigaru4")   # → "足軽４"
# ═══════════════════════════════════════════════════════════════════════════════

declare -A AGENT_NAME_JA=(
    [shogun]="将軍"
    [karo]="家老"
    [gunshi]="軍師"
    [ashigaru1]="足軽１"
    [ashigaru2]="足軽２"
    [ashigaru3]="足軽３"
    [ashigaru4]="足軽４"
    [ashigaru5]="足軽５"
    [ashigaru6]="足軽６"
    [ashigaru7]="足軽７"
)

# 英語のエージェント名を日本語に変換。未登録の場合は元の値を返す。
to_ja() {
    local key="$1"
    echo "${AGENT_NAME_JA[$key]:-$key}"
}
